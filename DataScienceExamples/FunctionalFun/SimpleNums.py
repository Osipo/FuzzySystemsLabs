def isSimple(n):
    if(n==2): return True
    if(n&1==0): return False
    r = sqrt(n)
    i = 3
    while i<r:
        if(n%i==0): return False
        i+=2
        if(i&3==1): i+=2
    return True
print(isSimple(19))